public class Client {

    public String name;
    public int id;
    public float balance;
    public String issue;
    public boolean isActive;

    public Client(String name, int id, float balance, String issue, boolean isActive) {
        this.name = name;
        this.id = id;
        this.balance = balance;
        this.issue = issue;
        this.isActive = isActive;
    }
}
